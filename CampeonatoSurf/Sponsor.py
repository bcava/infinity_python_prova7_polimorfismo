class Sponsor:
    def __init__(self, nomeSponsor, valor):
        self._nomeSponsor = nomeSponsor
        self._valor = valor
    
    
    @property
    def nomeSponsor(self):
        return self._nomeSponsor
    nomeSponsor.setter
    def nomeSponsor(self, nomeSponsor):
        self.nomeSponsor = nomeSponsor
    
    
    @property
    def valor(self):
        return self._valor
    valor.setter
    def valor(self, valor):
        self.valor = valor